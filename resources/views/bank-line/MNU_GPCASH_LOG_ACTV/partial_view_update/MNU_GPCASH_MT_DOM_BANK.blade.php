    function getDetail(id){
        var pendingTaskId_id= $('#pendingTaskId').val();
        var url_action= 'detailPendingTask';
         var value ={
            pendingTaskId:pendingTaskId_id,
            currentPage: "1",
            pageSize: "20",
            orderBy: {"code": "ASC"}
        };
        var action = 'DETAIL';
        $.ajax({
            url: 'getAPIData',
            method: 'post',
            data: {
                value : value,
                menu : id,
                url_action : url_action,
                action : action,
                _token : '{{ csrf_token() }}'
            },
            success: function (data) {

                var result = JSON.parse(data);
                if (result.status=="200") {
                    var detail = result.details;
                    $('#code_1').text(detail.code);
                    $('#name').text(detail.name);
                    $('#address1').text(detail.address1);
                    $('#address2').text(detail.address2);
                    $('#address3').text(detail.address3);
                    $('#organizationUnitCode').text(detail.organizationUnitCode);
                    $('#organizationUnitName').text(detail.organizationUnitName);
                    $('#memberCode').text(detail.memberCode);
                    $('#interBankCode').text(detail.biFastCode);
                    if(detail.bankCityList){
                        $.each(detail.bankCityList, function (idx, obj){
                            oTable.row.add([
                                obj.name,
                            ]).draw(true);
                        });
                        $('.cityDetail').show();
                        $('.domDetail').hide();
                    }else{
                        $('#address1').text(result.details.address1);
                        $('#address2').text(result.details.address2);
                        $('#address3').text(result.details.address3);

                        $('#organizationUnitName').text(result.details.organizationUnitName);
                        $('#memberCode').text(result.details.memberCode);
                        $('#biFastCode').text(result.details.biFastCode);
                        $('.cityDetail').hide();
                        $('.domDetail').show();
                    }



                    var detail = result.oldValue.details;
                    $('#old_code_1').text(detail.bankCode);
                    $('#old_name').text(detail.name);
                    $('#old_address1').text(detail.address1);
                    $('#old_address2').text(detail.address2);
                    $('#old_address3').text(detail.address3);
                    $('#old_organizationUnitCode').text(detail.organizationUnitCode);
                    $('#old_organizationUnitName').text(detail.organizationUnitName);
                    $('#old_memberCode').text(detail.memberCode);
                    $('#old_interBankCode').text(detail.biFastCode);
                    if(result.details.bankCityList){
                        $.each(detail.bankCityList, function (idx, obj){
                            old_oTable.row.add([
                                obj.name,
                            ]).draw(true);
                        });
                        $('.old_cityDetail').show();
                        $('.old_domDetail').hide();
                    }else{
                        $('#old_address1').text(detail.address1);
                        $('#old_address2').text(detail.address2);
                        $('#old_address3').text(detail.address3);

                        $('#old_organizationUnitName').text(detail.organizationUnitName);
                        $('#old_memberCode').text(detail.memberCode);
                        $('#old_biFastCode').text(detail.biFastCode);
                        $('.old_cityDetail').hide();
                        $('.old_domDetail').show();
                    }


                } else {
                    flash('warning', result.message);
                }

            }, error: function (xhr, ajaxOptions, thrownError) {
                var msg = '{{trans('form.conn_error')}}';
                flash('warning', msg);
                console.log(xhr.status + " ," + " " + ajaxOptions + ", " + thrownError);
            },
            complete: function(data) {

            }
        });
    }

</script>
